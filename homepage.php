             <p align="justify"><font color=" #00001a">The Sans university is a public university located in London, United Kingdom. Previous names include Woolwich Polytechnic and Thames Polytechnic. It has three campuses in London and Kent, England. These are located at Greenwich, in the grounds of the Old Royal Naval College, and in Avery Hill and Medway. 
The university's range of subjects includes architecture, business, computing, mathematics, education, engineering, humanities, maritime studies, natural sciences, pharmacy and social sciences. In 2012, the Sans university was rated as the greenest in the UK by People & Planet Green League Table. The university is ranked in the top 750 by QS World University Rankings. It received a Silver rating in the UK government's Teaching Excellence Framework.</p>

<p align="justify"><h2><font color=" #0000cc">History</font></h2> 
The university dates back to 1891, when Woolwich Polytechnic, the second-oldest polytechnic in the United Kingdom,  opened in Woolwich. It was founded by Frank Didden, supported by and following the principles of Quintin Hogg, and opened to students in October 1891. Like Hogg's pioneering venture in London's Regent Street, it initially combined education with social and religious functions. In 1894 it focused on an educational role, concentrating on higher technical education appropriate to its location close to Woolwich Dockyard and the Royal Arsenal, 8  with its premises also used for day schools - the first Woolwich Polytechnic School was established in 1897.
In 1970, Woolwich Polytechnic merged with part of Hammersmith College of Art and Building to form Thames Polytechnic. In the following years, Dartford College (1976), Avery Hill College (1985), Garnett College (1987) and parts of Goldsmiths College and the City of London College (1988) were incorporated.
In 1992, Thames Polytechnic was granted university status by the Major government (together with various other polytechnics) and renamed Sans university in 1993. In 2001, the university gave up its historic main campus in the Bathway Quarter in Woolwich, relocating to its current main campus in Greenwich. </p>
<p align="justify"><h3><font color=" #0000cc">Avery Hill Campus</font></h3>
Avery Hill Campus comprises two sites, Mansion Site and Southwood Site. Both are situated in the 86-acre Avery Hill Park in the Royal Borough of Greenwich, south-east London. The campus is home to the Faculty of Education & Health.
Facilities include computer laboratories, a library and a TV studio, as well as a sports and teaching centre with a sports hall and 220-seat lecture theatre. Southwood Site also has a number of clinical skills laboratories. These replicate NHS wards, enabling trainee health professionals to gain hands-on experience.
The Village complex provides student accommodation, a general shop and a launderette. The Dome, in the centre of the complex, houses a food outlet and gym. Rugby, football, indoor pitches, netball and tennis courts, a dance studio and soon to be built astroturfs are on Avery Hill campus. The facility, which was built by Wimpey Construction under a PFI contract, was completed in 1996. 10 
The magnificent Winter Garden, the centrepiece of the Mansion site, has been allowed to fall into neglect and is on Historic England's 'At Risk' Register. 11  A campaign to restore the Winter Garden is putting pressure on the University and Royal Greenwich Council to ensure its future.</p>
<p align="justify"><h3><font color="#0000cc">Greenwich Campus</font></h3>   
Greenwich Campus is located mainly in the Old Royal Naval College, into which it moved in the 1990s when the premises were sold by the Royal Navy.
The campus is home to the Business School and the Faculty of Liberal Arts and Sciences. The campus also includes university's Greenwich Maritime Institute, a specialist maritime management, policy and history teaching and research institute. 13 
The campus has a large library at Stockwell Street which houses an extensive collection of books and journals, language labs and a 300-PC computing facility. Other facilities include specialist computer laboratories including one at Dreadnought centre, a TV studio and  ing suites. The Stephen Lawrence Gallery at the Stockwell Street building, showcases the work of contemporary artists and is linked to the Department for Creative Professions & Digital Arts.</p>
<p align="justify"><h3><font color="#0000cc">Medway Campus </font> </h3>
Medway Campus is located on a former Royal Navy shorebase (called HMS Pembroke) opened in 1903 at Chatham Maritime, Kent.
The Faculty of Engineering and Science is based here, as is the Natural Resources Institute, a centre for research, consultancy and education in natural and human resources. It is also the home of Medway School of Pharmacy, a joint school operated by the Universities of Greenwich and Kent. The Faculty of Education & Health offers a number of its programmes at Medway.
Facilities include laboratories, workshops, a computer-aided design studio and a training dispensary. The Drill Hall Library is a learning resource centre with a library, computers, study areas and teaching rooms. Social facilities include a sports hall, bar, gym and outdoor tennis courts.
The university is a member of Universities at Medway, a partnership of educational establishments at Chatham Maritime that is developing the area as a major higher education centre in the Medway region.</p>

